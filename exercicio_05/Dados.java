package exercicio_05;

public class Dados {
    private int a, b;
    private float c, d;

    public Dados(int a, int b){
        this.a = a;
        this.b = b;
    }

    public Dados(float c, float d){
        this.c = c;
        this.d = d;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public float getC() {
        return c;
    }

    public float getD() {
        return d;
    }
}
